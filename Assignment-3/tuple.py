# create tuple element 
tuple = (1,2,3,4,5)
print(tuple)
# accessing the element in tuple
print("first element", tuple[0])
print("last element", tuple[-1])
print("slicing tuple", tuple[1:4])
# nested tuple
tuple = ("hello", (1,2,3), ("python, programming"))
print(tuple[0],[1])
# repetition of tuple
tuple = (1,2,3)*3
print(tuple)
# concatenated of tuple
tuple1 = (1,2,3)
tuple2 = (5,6,7)
print(tuple1+tuple2)